$('#btn-disco1').on('click', function () {
    
    $("#popup-disco1").css("opacity", "1");
    $("#popup-disco1").css("display", "block");
    $(".fondo").css("display", "block");
    $(".fondo").css("opacity", "1");
    
  });

$('.cerrar-popup').on('click', function () {
    
    $("#popup-disco1").css("opacity", "0");
    $("#popup-disco1").css("display", "none");
    $(".fondo").css("display", "none");
    $(".fondo").css("opacity", "0");
    
  });



